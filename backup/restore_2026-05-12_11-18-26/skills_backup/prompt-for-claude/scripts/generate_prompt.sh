#!/bin/bash

# Скрипт-генератор эффективных промптов для взаимодействия с Джарвисом (Claude 4.x через OpenClaw)
# Использует лучшие практики prompting: роль, контекст, примеры, формат, ограничения
# Сохраняет результат в файл или выводит в stdout

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SKILL_DIR="$(dirname "$SCRIPT_DIR")"
VAULT_ROOT="/root/vault"
OUTPUT_FILE="${VAULT_ROOT}/tmp/generated_claude_prompt_$(date +%Y%m%d_%H%M%S).md"
TEMP_FILE="/tmp/claudeprompt_$$"

# Создаём директорию для вывода, если её нет
mkdir -p "$(dirname "$OUTPUT_FILE")"

# Функция для вывода подсказки и чтения ответа
ask() {
  local prompt="$1"
  local default="$2"
  if [[ -n "$default" ]]; then
    read -rp "$prompt [$default]: " answer
    echo "${answer:-$default}"
  else
    read -rp "$prompt: " answer
    echo "$answer"
  fi
}

# Функция для вывода секции
section() {
  echo -e "\n### $1\n" >> "$TEMP_FILE"
}

# Функция для добавления содержимого
add_content() {
  local content="$1"
  if [[ -n "$content" && "$content" != "пропустить" && "$content" != "skip" && "$content" != "" ]]; then
    echo "$content" >> "$TEMP_FILE"
    echo "" >> "$TEMP_FILE"
  fi
}

# Начало
clear
echo "🎯 Генератор эффективных промптов для Джарвиса"
echo "Ответь на вопросы ниже — я сгенерирую структурированный промпт, который ты сможешь использовать для точного и релевантного ответа."
echo "Нажми Enter, чтобы пропустить необязательный вопрос."
echo ""

# 1. Роль
ROLE=$(ask "Кем я должен быть в этом контексте? (например: агроном-консультант, инженер по автоматизации, научный редактор)" "")
if [[ -n "$ROLE" ]]; then
  section "Роль"
  echo "Ты — $ROLE с опытом работы в соответствующей области." >> "$TEMP_FILE"
fi

# 2. Основная задача
TASK=$(ask "Что именно ты хочешь, чтобы я сделал? (объясни, рассчитай, проанализировал, разработал, суммаризировал и т.д.)" "")
if [[ -n "$TASK" ]]; then
  section "Задание"
  echo "$TASK" >> "$TEMP_FILE"
fi

# 3. Контекст
CONTEXT=$(ask "Какой контекст или фоновая информация поможет мне лучше понять задачу? (цель проекта, аудитория, ограничения)" "")
add_content "Контекст: $CONTEXT"

# 4. Мотивация
MOTIVATION=$(ask "Почему это важно? Кто будет использовать результат и как?" "")
add_content "Мотивация: $MOTIVATION"

# 5. Примеры (few-shot)
echo ""
echo "Хочешь ли ты привести примеры ожидаемого формата ответа? (рекомендуется для неочевидных форматов)"
EXAMPLES_ANSWER=$(ask "Введи примеры (можно несколько, разделённых пустой строкой), или напиши 'пропустить'" "")

if [[ "$EXAMPLES_ANSWER" != "пропустить" && "$EXAMPLES_ANSWER" != "skip" && -n "$EXAMPLES_ANSWER" ]]; then
  section "Примеры ожидаемого формата"
  echo "$EXAMPLES_ANSWER" >> "$TEMP_FILE"
  echo "" >> "$TEMP_FILE"
fi

# 6. Формат вывода
FORMAT=$(ask "Как должен выглядеть итоговый ответ? (например: Markdown с заголовками, JSON, таблица, plain text, только число и т.д.)" "")
if [[ -n "$FORMAT" ]]; then
  section "Формат вывода"
  echo "$FORMAT" >> "$TEMP_FILE"
fi

# 7. Ограничения и запреты
echo ""
echo "Есть ли ограничения на мои действия или ответ?"
LIMITS=$(ask "Например: не предлагать установку ПО, не выполнять системные команды, только анализ, не использовать markdown и т.д." "")
if [[ -n "$LIMITS" ]]; then
  section "Ограничения"
  echo "$LIMITS" >> "$TEMP_FILE"
fi

# 8. Длина и детализация
LENGTH=$(ask "Какой объём и глубину ответа ты ожидаешь? (например: 3-4 абзаца, только ключевые моменты, детальный разбор с методологией и ограничениями)" "")
if [[ -n "$LENGTH" ]]; then
  section "Ожидаемая длина и детализация"
  echo "$LENGTH" >> "$TEMP_FILE"
fi

# 9. Источники и проверка фактов
SOURCES=$(ask "Нужно ли подтверждать утверждения ссылками на источники? (например: arXiv, OIV, FAO, документация, peer-reviewed статьи)" "")
if [[ -n "$SOURCES" ]]; then
  section "Источники и проверка фактов"
  echo "$SOURCES" >> "$TEMP_FILE"
fi

# 10. XML-структура (опционально для сложных кейсов)
echo ""
echo "Если твой запрос включает данные, несколько частей или сложную структуру — хочешь ли я предложить XML-формат?"
XML_ASK=$(ask "Напиши 'да', если хочешь шаблон с XML-тегами для вставки данных, контекста и т.д., или 'нет' чтобы пропустить" "")
if [[ "$XML_ASK" =~ ^[дД]а|да|yes|y$ ]]; then
  section "XML-структура (шаблон для сложных запросов)"
  cat >> "$TEMP_FILE" << 'EOF'

<instructions>
  [Здесь чётко и конкретно опиши, что ты хочешь, чтобы я сделал]
</instructions>
<context>
  [Здесь фоновая информация: цель, аудитория, ограничения, источник данных]
</context>
<input>
  [Здесь данные, если они нужны: таблица, список, текст, параметры и т.д.]
</input>
<format>
  [Здесь опиши ожидаемый формат ответа: структуру, типы, единицы, точность]
</format>
<constraints>
  [Здесь ограничения: не выполнять действия, только анализ, проверить источники, длина ответа и т.д.]
</constraints>
</request>
EOF
  echo "" >> "$TEMP_FILE"
fi

# Финальное сообщение и вывод
echo -e "\n✅ Промпт сгенерирован!"
echo "Результат сохранён в: $OUTPUT_FILE"
echo ""

# Копируем временный файл в最終ный
cp "$TEMP_FILE" "$OUTPUT_FILE"

# Выводим результат
echo "=== СГЕНЕРИРОВАННЫЙ ПРОМПТ ==="
echo ""
cat "$OUTPUT_FILE"
echo ""
echo "=============================="

echo ""
echo "💡 Советы по использованию:"
echo "   - Скопируй промпт и вставь его в наш чат перед сложным запросом"
echo "   - Сохраняй лучшие варианты в /root/vault/prompts/ или в своём знани vault"
echo "   - После использования отметь, что сработало — улучшай скрипт со временем"
echo "   - Скрипт расположен: $SCRIPT_DIR/generate_prompt.sh"

# Очистка
rm -f "$TEMP_FILE"

exit 0